  <h1>Anxiety and Stress</h1>
  
  <p>We all experience stress in our lives but sometimes it becomes too much, and we can begin to feel anxious and worried about the smallest of things to the point where it begins to affect our daily functioning. Hypnotherapy works with the subconscious mind to get to the causes of those behaviours enabling you to let go and relax again.</p>
  
  <h4>What is Anxiety?</h4>
  
  <p>Anxiety is an uncontrollable and often irrational worrying about situations, events or everyday events that can be out of proportion to the situation.  You may find yourself anticipating disaster or catastrophising around situations that a part of you knows is really ok.  Perhaps you are finding it difficult to stop worrying about finances, exams or tests, job interviews, being on time, health issues or family problems to the point where it is beginning to interfere with your life.</p>
  
  <h4>What does Anxiety feel like?</h4>
  
  <p>Anxiety is an emotional reaction to our thoughts and feelings.  Situations do not automatically create anxiety, rather it is our reaction to them or how we think about them.  This emotional reaction, in turn, creates a body response producing a variety of physical symptoms including fatigue, insomnia, irritability, sweating, a sense of panic or agitation, difficulties breathing, muscle aches or difficulty swallowing.  </p>
  
  <h4>The Effects of Anxiety</h4>
  
  <p>Most people have been anxious at some time in their life and experienced these symptoms and other symptoms. Anxiety can be a useful tool in recognising a stress in our lives.  However, problems arise when the anxiety continues or comes back in a stronger form each time it is experienced.  Continued or chronic anxiety may lead to such conditions as irritable bowel syndrome, tension headaches or migraine, compulsive or obsessive behaviours, social anxiety and many more. It can be hard, at times, to pin point where this anxiety is coming from or, once located, hard to know how to stop it.  His is where hypnotherapy, a combination of counselling and hypnotherapy, can help. </p>
  
  <h4>How Hypnotherapy can help</h4>
  
  <p>Hypnotherapy has been shown to be effective in the treatment of anxiety by inducing a calming and relaxing state of mind and body, enabling the body to counteract the effects of increased levels of adrenalin in the system. Hypnotherapy goes further than just pleasant relaxation.  By exploring the root causes of the anxiety and dealing with any emergent concerns, anxiety and the resultant stress can be reduced.</p>
  
  <h4>STRESS</h4>
  
  <p>Stress is something we have all experienced.  A certain level of stress is good as it can move us into action and/or create the impetus to bring change into our lives.  Too much stress, however, can have negative effects on our bodies and emotional health, leading to a variety of illness or general malaise.  There are many things we can do to reduce stress but sometimes we can be too stressed to do the things we know we need to do.  In these situations, hypnotherapy can be a useful tool. Hypnotherapy provides opportunities to let go of stress and the anxiety around it. Through discussion you are able to get to the root cause of this stress and manage it with hypnosis, creating a release.</p>
  

  <h3>WHAT IS TRAUMA?</h3>

  <p>Have you been through an event in your life which has left a mark or a scar?  No matter how big or small, these events have an impact on behaviour and how you view yourself in the world.  It may weigh you down or cause you to have feelings of helplessness; a sense that you have little or no control how you react in some situations.</p>

  <p>Trauma are those events in your life that have caused unhappiness and stress.  The younger you were when these events happened the more likely you are to have developed behaviours which 'just pop out'.  You may realise your behaviour is not rational but it still 'just happens'.  Perhaps you find yourself suddenly getting angry or crying when in particular circumstances.  Are you being 'triggered' by things from the past?  Things which have no relevance in the present?</p>

  <p>Trauma from the past can get in the way of living your life in the present.  It can drag you back into ways of behaving that you know are detrimental to your relationships, your work or what you allow yourself to accomplish.  At times you may be aware of this but often it's such an established pattern it feels like an old pair of slippers - comfortable and familiar and what you know.</p>

  <p>Hypnosis is able to take most people to a point where you can process and let go of these events from the past.  It does it quickly and simply without the need to revisit or explore in detail the actual events.  You just 'let it go'.  Instead of viewing your world through those old hurts, hypnosis allows you to leave it in the past where it belongs and engage more fully in the present.</p>

  <p><em>If you believe you are being affected by trauma or past events, please contact me for a confidential discussion to determine if hypnotherapy is an appropriate form of treatment for you.</em></p>

